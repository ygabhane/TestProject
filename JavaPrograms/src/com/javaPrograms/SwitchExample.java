package com.javaPrograms;

public class SwitchExample {
	public static void main(String[]args)
	{
		int number=20;
		switch(number)
		{
		case 10: System.out.println("Number is available in 10 ");
		break;
		case 20: System.out.println("Number is available in 20");
		break;
		case 30: System.out.println("Number is available in 30 ");
		break;
		default : System.out.println("Number is not in 10,20,30");
		}
		
	}

}
